vector<int> edges[N];
int col[N];
void dfs(int u, int p, int c) {
  col[u] = c;
  for (int i = 0; i < edges[u].size(); ++i) {
    int v = edges[u][i];
    if (v == p) continue;
    dfs(v, u, !c);
  }
}
